.MODEL SMALL
.STACK 100H
.386
.DATA

PROD1 DD ?
PROD2 DD ?

DATA1 DD 00000000H
DATA2 DD 00000000H

str1 db 10,13,"Enter first number: $"
str2 db 10,13,"Enter second number: $"
PROD db 10,13,"The PRODUCT is: $"

.CODE
.startup
	
	MOV AH,09
	MOV DX,OFFSET str1
	INT 21H
	
	MOV EBX,0
	MOV ECX,8
	AGAIN:
		MOV AH,01
		INT 21H
		
		CMP AL,'A'
		JGE L1
		SUB AL,30H
		JMP L2
		L1: SUB AL,37H
		L2: SHL EBX,4
		ADD BL,AL
		LOOP AGAIN
	MOV DATA1,EBX
	
	MOV AH,09
	MOV DX,OFFSET str2
	INT 21H
	
	MOV EBX,0
	MOV ECX,8
	AGAIN2:
		MOV AH,01
		INT 21H
		
		CMP AL,'A'
		JGE L3
		SUB AL,30H
		JMP L4
		L3: SUB AL,37H
		L4: SHL EBX,4
		ADD BL,AL
		LOOP AGAIN2
	MOV DATA2,EBX
	
	MOV EBX,0
	MOV EAX,0
	MOV EDX,0
	MOV EAX,DATA1
	MOV EBX,DATA2
	MUL EBX
	MOV PROD1,EDX
	MOV PROD2,EAX
	
	MOV AH,09
	MOV DX,OFFSET PROD
	INT 21H
	
	MOV EBX,PROD1
	
	MOV ECX,8
	AGAIN3:
		ROL EBX,4
		MOV DL,BL
		AND DL,0FH
		CMP DL,9
		JB P1
		ADD DL,37H
		JMP P2
		P1:ADD DL,30H
		P2: MOV AH,02
		INT 21H
		LOOP AGAIN3
	
	MOV EBX,PROD2
	
	MOV ECX,8
	AGAIN4:
		ROL EBX,4
		MOV DL,BL
		AND DL,0FH
		CMP DL,9
		JB P3
		ADD DL,37H
		JMP P4
		P3:ADD DL,30H
		P4:MOV AH,02
		INT 21H
		LOOP AGAIN4
	
	MOV AH,4CH
	INT 21H
END
		
		
